import numpy as np

#-----------------------------------------------------#
#                   Softmax Function                  #
#-----------------------------------------------------#
def softmax(x):
    """Compute softmax values for each sets of scores in x."""
    e_x = np.exp(x - np.max(x))
    return e_x / e_x.sum()
